public class Variables {

    public static void main(String[] args) {
        String nama;
        nama = "Dyaksa Jauharuddin Nour";

        int value = 100;

        System.out.println(nama);
        System.out.println(value);

        var firsName = "Dyaksa";
        var middleName = "Jauharuddin";
        var lastName = "Nour";

        System.out.println(firsName + middleName + lastName);

        final long activeIncome = 25_000_000_000L;
        System.out.println(activeIncome);
    }
}
