public class OperasiMatematika {

    public static void main(String[] args) {
        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        int c = 10;

        System.out.println(c += 10);
        System.out.println(c -= 10);
        System.out.println(c *= 10);
        System.out.println(c /= 10);

        int d = +100;
        int e = -10;

        System.out.println(d++);
        System.out.println(e--);
        System.out.println(!true);
    }
}
